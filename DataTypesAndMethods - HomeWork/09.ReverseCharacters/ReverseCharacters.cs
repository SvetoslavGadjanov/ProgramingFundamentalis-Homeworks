using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _09.ReverseCharacters
{
    class ReverseCharacters
    {
        static void Main(string[] args)
        {
            char first = Convert.ToChar(Console.ReadLine());
            char second = Convert.ToChar(Console.ReadLine());
            char third = Convert.ToChar(Console.ReadLine());

            

            Console.Write((char)third);
            Console.Write((char)second);
            Console.Write((char)first);
            Console.WriteLine();


        }
    }
}
